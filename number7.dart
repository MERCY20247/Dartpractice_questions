//7. Write a program that takes two numbers and prints their:
// a. Sum
// b. Difference
// c. Product
// d. Quotient (division result)

void main() {
  int num1 = 10;
  int num2 = 5;

  print('Sum: ${num1 + num2}');
  print('Difference: ${num1 - num2}');
  print('Product: ${num1 * num2}');
  print('Quotient: ${num1 / num2}');
}
